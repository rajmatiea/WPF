//Circumference
//Calculate the circumference of a circle

//function parameters
function circleCircum(r, d) {
//variable declaration
    var r = 30;
    var d = 25;
    
//calculating the area of the cirle     
    var circumfrence = r * d;
    //print to console
    console.log("The circumference of the circle is " + circumfrence);
    //returning value
    return circumfrence;
}
//function call
circleCircum();



